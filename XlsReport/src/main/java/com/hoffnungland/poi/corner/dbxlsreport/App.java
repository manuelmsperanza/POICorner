package com.hoffnungland.poi.corner.dbxlsreport;

/**
 * Entry point utilities for the XlsReport module.
 */
public class App {

    /**
     * Builds the default greeting message.
     *
     * @return greeting text used by {@link #main(String[])}
     */
    static String greeting() {
        return "Hello World!";
    }

    /**
     * Runs the console entry point.
     *
     * @param args CLI arguments
     */
    public static void main(String[] args) {
        System.out.println(greeting());
    }
}
